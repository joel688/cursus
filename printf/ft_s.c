#include "ft_printf.h"

int	ft_s(char *ap)
{
	int	i;

	i = 0;
	if (ap == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (ap[i])
	{
		write(1, &ap[i], 1);
		i++;
	}
	return (i);
}
